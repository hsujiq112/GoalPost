import UIKit

extension UIButton {
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4284670949, green: 0.6749560237, blue: 0.1872602403, alpha: 1)
    }
    
    func setDeselectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4520945549, green: 0.7302217484, blue: 0.2031045258, alpha: 0.5)
    }
}
